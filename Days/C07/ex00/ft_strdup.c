#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*dest = NULL;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(&src[i]);
	dest = malloc(sizeof(char) * len + 1);
	if (dest == NULL)
		return (0);
	if (!(src))
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

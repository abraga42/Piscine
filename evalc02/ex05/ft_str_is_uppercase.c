int	ft_str_is_uppercase(char *str)
{
	int	a;

	a = 0;
	while (str[a] >= 'A' && str[a] <= 'Z')
	{
		a++;
	}
	if (str[a] == 0)
		return (1);
	else
		return (0);
}
